public class Notecard {
    private String front;
    private String back;

    public Notecard()
    {

    }
    public Notecard(String f, String b)
    {
        front = f;
        back = b;
    }
    public String GetFront()
    {
        return front;
    }
    public String GetBack()
    {
        return back;
    }

}
